/*this line crate a variable name kelvin and stores value of 293 and it is constant value.*/
const kelvin = 0;
/*in this below line we subtract kelvin value by 273 and store in celcius*/
var celcius = kelvin - 273;
/*this line we multiply celcius by (9/5) ans add 32 on it and stores this value into farhenheit*/
let farhenheit = celcius * (9 / 5) + 32;
/* in this line we convert decimal value into integer value*/
farhenheit = Math.floor(farhenheit);
console.log(`The temperature is ${farhenheit} degrees Farenheit`);



